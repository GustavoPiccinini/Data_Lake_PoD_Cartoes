# Definir o esquema da tabela e corrigir os tipos de dados

spark.sql("""
   select
      cast(split(id_fatura, '-')[1] as bigint) as id_fatura,
      cast(id_cliente as bigint) as id_cliente,
      cast(data_emissao as date) as data_emissao,
      cast(data_vencimento as date) as data_vencimento,
      cast(valor_fatura as decimal(15,2)) as valor_fatura,
      cast(valor_pagamento_minimo as decimal(15,2)) as valor_pagamento_minimo
   from df_fatura
""").show()

"""**Variáveis de Processamento e Referência
Organizam e estruturam os dados, facilitando consultas, análises e integração com ferramentas de ciência de dados, aumentando a eficiência e a clareza do Data Lake**
"""

#Variáveis de processamento e referência
dt_proc = datetime.now().strftime('%Y%m%d%H%M%S')

df_fatura_format = spark.sql(f"""
   select
      cast(split(id_fatura, '-')[1] as bigint) as id_fatura,
      cast(id_cliente as bigint) as id_cliente,
      "{dt_proc}" as dt_proc,
      substring(replace(data_emissao,'-',''),1,6) ref,
      cast(data_emissao as date) as data_emissao,
      cast(data_vencimento as date) as data_vencimento,
      cast(valor_fatura as decimal(15,2)) as valor_fatura,
      cast(valor_pagamento_minimo as decimal(15,2)) as valor_pagamento_minimo
   from df_fatura
""")
df_fatura_format.createOrReplaceTempView('df_fatura_format')

"""**Salvando os dados na Camanda Trusted**"""

df_fatura_format.write.mode('append').partitionBy('ref').parquet('/content/gdrive/MyDrive/PodCartoes/002_trusted/tb_01fatura')

tb_fatura= spark.read.parquet('/content/gdrive/MyDrive/PodCartoes/002_trusted/tb_02pagamento')
tb_fatura.createOrReplaceTempView('tb_fatura')

"""**Carregamento dos dados da pasta controle**"""


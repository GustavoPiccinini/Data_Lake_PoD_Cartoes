#Criar uma função para registrar a data atual
def log():
  return datetime.now().strftime('%Y-%m-%d %H:%M:%S') +' >>>'

log()

"""**Iniciando o processamento da camada Trusted com o arquivo de Faturas**"""

df_fatura = spark.read.option('header','True').csv('/content/gdrive/MyDrive/PodCartoes/001_raw/tb_faturas_20241201_20241202000000.csv')
